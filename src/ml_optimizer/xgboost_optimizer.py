"""
src/ml_optimizer/xgboost_optimizer.py
=======================================
XGBoost surrogate model for race time prediction.

Engineering responsibility:
    Train a regression surrogate that approximates simulate_strategy()
    with 1,000× lower inference cost, enabling large-scale strategy
    optimisation that would be prohibitively slow with the full simulator.

Surrogate model pattern:
    The full physics simulator (race_simulator.py) is:
        Accurate: captures fuel load, degradation physics, pit mechanics.
        Slow:     < 1ms per call, but 50,000 calls = 50 seconds.

    The XGBoost surrogate is:
        Approximate: trained to mimic simulator output on a training set.
        Fast:       ~1µs per call → 50,000 calls = 0.05 seconds (1000× faster).
        Bounded:    only as good as the training distribution covers.

    The surrogate is used for:
        (a) INITIAL SWEEP: evaluate all ~5,000 1/2/3-stop strategies
            cheaply to identify the top-100 candidates.
        (b) FINAL EVALUATION: re-run top candidates through the full
            physics simulator for high-fidelity ranking.

    This "coarse-to-fine" approach combines the speed of ML with the
    accuracy of physics simulation — the same pattern used in
    aerospace wing optimisation and CFD surrogate modelling.

Training data generation:
    The surrogate is trained on SIMULATOR OUTPUTS — not raw FastF1 data.
    This is the key design decision. The simulator already incorporates
    the degradation model, fuel physics, and pit stop mechanics.
    Training on simulator outputs means:
        - The surrogate learns the simulator's input-output mapping.
        - It interpolates between strategies the simulator evaluated.
        - It does NOT need to re-learn physics already encoded in the sim.

    Training data is generated by running enumerate_strategies() across
    a grid of circuit/season combinations, simulating each one, and
    collecting (strategy_features, total_race_time) pairs.

Strategy feature encoding:
    A strategy must be encoded as a fixed-length numeric vector for ML.
    The encoding used:
        [pit_lap_1_normalised, pit_lap_2_normalised, pit_lap_3_normalised,
         compound_1_encoded, compound_2_encoded, compound_3_encoded,
         compound_4_encoded,
         n_stops, race_laps, base_lap_time,
         sc_probability, mean_deg_rate, is_high_deg, is_street]

    Pit laps are normalised by race_laps (0=lap1, 1=final lap).
    0 for unused pit slots (0-stop: all pit_laps = 0).
    Compounds encoded as ordinal: SOFT=0, MEDIUM=1, HARD=2.
    This encoding preserves the ordinal relationship (S < M < H in speed).

XGBoost hyperparameters:
    n_estimators=500:   More trees → better fit, diminishing returns >500.
    max_depth=6:        Shallow enough to prevent overfitting on structured
                        tabular data with ~20k training samples.
    learning_rate=0.05: Slow learning rate with more trees is the standard
                        XGBoost recommendation for structured data.
    subsample=0.8:      Stochastic gradient boosting — reduces overfitting.
    colsample_bytree=0.8: Feature subsampling per tree.
    reg_lambda=1.0:     L2 regularisation to prevent overfitting on extreme
                        strategy combinations (e.g. 4-stop on 25-lap race).
"""

