# Compound ordinal encoding for ML feature vector.
# Ordinal (0/1/2) rather than one-hot because the ordinal relationship
# is meaningful: SOFT < MEDIUM < HARD in terms of pace/durability tradeoff.
COMPOUND_ORDINAL: dict[str, int] = {
    "SOFT":         0,
    "MEDIUM":       1,
    "HARD":         2,
    "INTERMEDIATE": 3,
    "WET":          4,
    "UNKNOWN":      2,  # Default to HARD (conservative assumption)
}

# Maximum number of stints encoded in the feature vector.
# 4 stints = 3-stop strategy = the maximum we model.
MAX_STINTS_ENCODED: int = 4

# Maximum number of pit laps encoded (= MAX_STINTS_ENCODED - 1).
MAX_PIT_LAPS_ENCODED: int = 3

# Number of features in the strategy encoding vector.
# See STRATEGY_FEATURE_NAMES for the canonical list.
N_STRATEGY_FEATURES: int = (
    MAX_PIT_LAPS_ENCODED      # normalised pit laps (0 if unused)
    + MAX_STINTS_ENCODED      # compound ordinals per stint
    + 1                       # n_stops
    + 1                       # race_laps
    + 1                       # base_lap_time_sec
    + 1                       # sc_probability
    + 1                       # mean_deg_rate
    + 1                       # is_high_deg_circuit
    + 1                       # is_street_circuit
)  # = 14

STRATEGY_FEATURE_NAMES: list[str] = (
    [f"pit_lap_{i}_norm" for i in range(1, MAX_PIT_LAPS_ENCODED + 1)]
    + [f"compound_{i}_ordinal" for i in range(1, MAX_STINTS_ENCODED + 1)]
    + ["n_stops", "race_laps", "base_lap_time_sec",
       "sc_probability", "mean_deg_rate",
       "is_high_deg_circuit", "is_street_circuit"]
)

# Number of training samples to generate per circuit/season combination.
TRAINING_SAMPLES_PER_CIRCUIT: int = 2000

# Cross-validation folds for surrogate model evaluation.
SURROGATE_CV_FOLDS: int = 5

# Acceptable surrogate MAE threshold (seconds).
# If the surrogate's CV MAE exceeds this, log a warning — the surrogate
# may not be reliable for fine-grained strategy differentiation.
MAX_ACCEPTABLE_MAE_SEC: float = 3.0

# XGBoost hyperparameters (defaults — can be overridden at training time).
XGBOOST_DEFAULTS: dict = {
    "n_estimators":      500,
    "max_depth":         6,
    "learning_rate":     0.05,
    "subsample":         0.8,
    "colsample_bytree":  0.8,
    "reg_lambda":        1.0,
    "reg_alpha":         0.1,
    "random_state":      42,
    "n_jobs":            -1,
    "verbosity":         0,
}

# Random state for reproducible training data sampling.
RANDOM_STATE: int = 42


