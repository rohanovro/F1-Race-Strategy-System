def surrogate_optimise(
    surrogate:           SurrogateModel,
    model_set:           DegradationModelSet,
    available_compounds: list[str],
    total_race_laps:     int,
    base_lap_time:       float,
    sc_probability:      float = 0.68,
    mean_deg_rate:       float = 0.04,
    is_high_deg:         int   = 0,
    is_street:           int   = 0,
    top_k_for_simulator: int   = 50,
    pit_lane_delta_sec:  float = 19.0,
) -> pd.DataFrame:
    """
    Surrogate-accelerated strategy optimisation.

    Two-phase approach:
        Phase 1 (FAST):  Score all enumerated strategies with the surrogate.
                         Sub-second for ~5,000 strategies.
        Phase 2 (EXACT): Re-evaluate top_k_for_simulator candidates with
                         the full physics simulator. Returns a leaderboard
                         with simulator-validated race times.

    The gap between surrogate rank and simulator rank is a direct measure
    of surrogate quality — logged for monitoring.

    Args:
        surrogate:           Fitted SurrogateModel.
        model_set:           DegradationModelSet for Phase 2 simulation.
        available_compounds: Dry compounds for strategy enumeration.
        total_race_laps:     Race distance.
        base_lap_time:       Reference lap time.
        sc_probability:      SC frequency.
        mean_deg_rate:       Mean degradation rate.
        is_high_deg:         High deg circuit flag.
        is_street:           Street circuit flag.
        top_k_for_simulator: Number of top surrogate candidates to validate.
        pit_lane_delta_sec:  Pit lane traverse time.

    Returns:
        DataFrame of Phase 2 results sorted by simulator total_time_sec.
        Columns: rank, label, compounds, pit_laps, surrogate_time_sec,
                 simulator_time_sec, surrogate_error_sec.
    """
    from src.strategy_engine.pit_window_optimizer import enumerate_strategies

    if not surrogate.is_reliable():
        logger.warning(
            "surrogate_optimise: surrogate MAE=%.2fs exceeds %.1fs threshold. "
            "Results may be unreliable. Consider retraining with more data.",
            surrogate.mean_cv_mae, MAX_ACCEPTABLE_MAE_SEC,
        )

    # Phase 1: Surrogate sweep
    logger.info("surrogate_optimise: Phase 1 — surrogate sweep...")
    all_strategies = enumerate_strategies(
        available_compounds = available_compounds,
        total_race_laps     = total_race_laps,
        pit_lane_delta_sec  = pit_lane_delta_sec,
    )

    surrogate_times = surrogate.predict(
        strategies      = all_strategies,
        total_race_laps = total_race_laps,
        base_lap_time   = base_lap_time,
        sc_probability  = sc_probability,
        mean_deg_rate   = mean_deg_rate,
        is_high_deg     = is_high_deg,
        is_street       = is_street,
    )

    # Sort by surrogate prediction and select top-k for Phase 2
    surrogate_order = np.argsort(surrogate_times)
    top_k_indices   = surrogate_order[:top_k_for_simulator]
    top_k_strategies = [all_strategies[i] for i in top_k_indices]
    top_k_surrogate  = surrogate_times[top_k_indices]

    logger.info(
        "surrogate_optimise: Phase 2 — simulating top %d candidates...",
        len(top_k_strategies),
    )

    # Phase 2: Full simulation of top-k
    rows = []
    for i, (strategy, surr_time) in enumerate(
        zip(top_k_strategies, top_k_surrogate)
    ):
        result = simulate_strategy(
            strategy          = strategy,
            model_set         = model_set,
            base_lap_time_sec = base_lap_time,
            total_race_laps   = total_race_laps,
        )
        if not result.is_valid:
            continue

        sim_time  = result.total_race_time_sec
        surr_err  = surr_time - sim_time
        compound_str = "-".join(
            COMPOUND_ABBREV.get(c, "?") for c in strategy.compounds_used
        )
        rows.append({
            "surrogate_rank":    i + 1,
            "label":             strategy.label,
            "n_stops":           strategy.n_stops,
            "compounds":         compound_str,
            "pit_laps":          ", ".join(str(p) for p in strategy.pit_laps) or "none",
            "surrogate_time_sec": round(surr_time, 3),
            "simulator_time_sec": round(sim_time,  3),
            "surrogate_error_sec": round(surr_err, 3),
        })

    if not rows:
        logger.error("surrogate_optimise: no valid Phase 2 results.")
        return pd.DataFrame()

    df = pd.DataFrame(rows).sort_values("simulator_time_sec").reset_index(drop=True)
    df.insert(0, "rank", range(1, len(df) + 1))

    mean_abs_err = df["surrogate_error_sec"].abs().mean()
    rank_corr    = df["surrogate_rank"].corr(df["rank"], method="spearman")

    logger.info(
        "surrogate_optimise: Phase 2 complete | "
        "mean_surrogate_error=%.2fs | rank_correlation=%.3f | "
        "optimal=%s (%s)",
        mean_abs_err, rank_corr,
        df.iloc[0]["label"],
        df.iloc[0]["simulator_time_sec"],
    )

    return df


