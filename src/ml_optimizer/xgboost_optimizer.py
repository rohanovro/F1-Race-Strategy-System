def save_surrogate(
    model:     SurrogateModel,
    save_path: Optional[Path] = None,
) -> Path:
    """Save SurrogateModel to disk."""
    if save_path is None:
        save_path = DATA_PROCESSED_DIR / "surrogate_model.pkl"
    save_path = Path(save_path)
    save_path.parent.mkdir(parents=True, exist_ok=True)
    with open(save_path, "wb") as f:
        pickle.dump(model, f)
    logger.info("save_surrogate: saved to %s", save_path)
    return save_path


def load_surrogate(load_path: Optional[Path] = None) -> SurrogateModel:
    """Load a previously serialised SurrogateModel."""
    if load_path is None:
        load_path = DATA_PROCESSED_DIR / "surrogate_model.pkl"
    load_path = Path(load_path)
    if not load_path.exists():
        raise FileNotFoundError(
            f"load_surrogate: no model at {load_path}. "
            "Run train_surrogate_model() first."
        )
    with open(load_path, "rb") as f:
        model = pickle.load(f)
    logger.info("load_surrogate: loaded | %s", model.summary())
    return model
