def encode_strategy(
    strategy:        RaceStrategy,
    total_race_laps: int,
    base_lap_time:   float,
    sc_probability:  float = 0.68,
    mean_deg_rate:   float = 0.04,
    is_high_deg:     int   = 0,
    is_street:       int   = 0,
) -> np.ndarray:
    """
    Encode a RaceStrategy as a fixed-length numeric feature vector.

    The encoding is designed to be:
        FIXED LENGTH: same size regardless of stop count (unused slots = 0).
        ORDINAL-CONSISTENT: compound encoding preserves speed ordering.
        NORMALISED: pit laps divided by race_laps → [0, 1].
        CONTEXT-AWARE: circuit and session features included.

    Args:
        strategy:        RaceStrategy to encode.
        total_race_laps: Race distance (for pit lap normalisation).
        base_lap_time:   Reference lap time in seconds.
        sc_probability:  Historical SC events per race at this circuit.
        mean_deg_rate:   Mean degradation rate across dry compounds.
        is_high_deg:     1 if high-degradation circuit.
        is_street:       1 if street circuit.

    Returns:
        1D numpy array of length N_STRATEGY_FEATURES.
    """
    # Pit lap slots (normalised, padded with 0 for unused slots)
    pit_laps_norm = [0.0] * MAX_PIT_LAPS_ENCODED
    for i, pl in enumerate(strategy.pit_laps[:MAX_PIT_LAPS_ENCODED]):
        pit_laps_norm[i] = pl / total_race_laps

    # Compound ordinals (padded with last-compound ordinal for unused stints)
    compounds    = strategy.compounds_used
    compound_enc = [0] * MAX_STINTS_ENCODED
    for i in range(MAX_STINTS_ENCODED):
        if i < len(compounds):
            compound_enc[i] = COMPOUND_ORDINAL.get(compounds[i].upper(), 2)
        else:
            # Pad with the last stint's compound (nearest-neighbour padding)
            compound_enc[i] = compound_enc[i - 1] if i > 0 else 1

    feature_vec = np.array(
        pit_laps_norm
        + compound_enc
        + [
            float(strategy.n_stops),
            float(total_race_laps),
            float(base_lap_time),
            float(sc_probability),
            float(mean_deg_rate),
            float(is_high_deg),
            float(is_street),
        ],
        dtype=np.float64,
    )

    assert len(feature_vec) == N_STRATEGY_FEATURES, (
        f"encode_strategy: feature vector length {len(feature_vec)} "
        f"!= expected {N_STRATEGY_FEATURES}. "
        f"STRATEGY_FEATURE_NAMES has {len(STRATEGY_FEATURE_NAMES)} entries."
    )
    return feature_vec


def encode_strategies_batch(
    strategies:      list[RaceStrategy],
    total_race_laps: int,
    base_lap_time:   float,
    sc_probability:  float = 0.68,
    mean_deg_rate:   float = 0.04,
    is_high_deg:     int   = 0,
    is_street:       int   = 0,
) -> np.ndarray:
    """
    Encode a list of strategies into a 2D feature matrix.

    Args:
        strategies:      List of RaceStrategy to encode.
        total_race_laps: Race distance.
        base_lap_time:   Reference lap time.
        sc_probability:  SC frequency for this circuit.
        mean_deg_rate:   Mean degradation rate.
        is_high_deg:     Circuit degradation flag.
        is_street:       Street circuit flag.

    Returns:
        2D array of shape (n_strategies, N_STRATEGY_FEATURES).
    """
    rows = [
        encode_strategy(
            s, total_race_laps, base_lap_time,
            sc_probability, mean_deg_rate, is_high_deg, is_street,
        )
        for s in strategies
    ]
    return np.vstack(rows)


