@dataclass
class SurrogateModel:
    """
    Trained XGBoost surrogate model container.

    Attributes:
        pipeline:            Fitted Pipeline (StandardScaler + XGBRegressor).
        feature_names:       Canonical feature names.
        cv_scores_mae:       Cross-validation MAE scores.
        mean_cv_mae:         Mean CV MAE (seconds).
        std_cv_mae:          Std dev of CV MAE.
        cv_scores_r2:        Cross-validation R² scores.
        mean_cv_r2:          Mean CV R².
        training_samples:    Number of training samples.
        feature_importances: Feature importance array (XGBoost gain).
        y_train_mean:        Mean of training target (for sanity checks).
        y_train_std:         Std of training target.
    """
    pipeline:            Pipeline
    feature_names:       list[str]
    cv_scores_mae:       np.ndarray
    mean_cv_mae:         float
    std_cv_mae:          float
    cv_scores_r2:        np.ndarray
    mean_cv_r2:          float
    training_samples:    int
    feature_importances: np.ndarray
    y_train_mean:        float
    y_train_std:         float

    def predict(
        self,
        strategies:      list[RaceStrategy],
        total_race_laps: int,
        base_lap_time:   float,
        sc_probability:  float = 0.68,
        mean_deg_rate:   float = 0.04,
        is_high_deg:     int   = 0,
        is_street:       int   = 0,
    ) -> np.ndarray:
        """
        Predict total race time for a list of strategies.

        This is the hot-path call during strategy sweep.
        Encodes all strategies in a single batch, then calls
        pipeline.predict() once — maximises XGBoost throughput.

        Args:
            strategies:      List of RaceStrategy to evaluate.
            total_race_laps: Race distance.
            base_lap_time:   Reference lap time.
            sc_probability:  SC frequency.
            mean_deg_rate:   Mean degradation rate.
            is_high_deg:     High deg flag.
            is_street:       Street circuit flag.

        Returns:
            Array of predicted total race times (seconds).
        """
        X = encode_strategies_batch(
            strategies, total_race_laps, base_lap_time,
            sc_probability, mean_deg_rate, is_high_deg, is_street,
        )
        X_df = pd.DataFrame(X, columns=self.feature_names)
        return self.pipeline.predict(X_df).flatten()

    def predict_single(
        self,
        strategy:        RaceStrategy,
        total_race_laps: int,
        base_lap_time:   float,
        **kwargs,
    ) -> float:
        """Predict total race time for a single strategy."""
        return float(self.predict([strategy], total_race_laps, base_lap_time, **kwargs)[0])

    def is_reliable(self) -> bool:
        """
        Return True if the surrogate's MAE is within acceptable bounds.

        A surrogate with MAE > MAX_ACCEPTABLE_MAE_SEC should not be used
        for fine-grained strategy differentiation (strategies separated
        by < 1s). The full simulator should be used instead.
        """
        return self.mean_cv_mae <= MAX_ACCEPTABLE_MAE_SEC

    def summary(self) -> str:
        reliable_str = "RELIABLE" if self.is_reliable() else "UNRELIABLE"
        return (
            f"SurrogateModel [{reliable_str}] | "
            f"n={self.training_samples} | "
            f"CV MAE={self.mean_cv_mae:.2f}±{self.std_cv_mae:.2f}s | "
            f"CV R²={self.mean_cv_r2:.4f}"
        )

    def top_features(self, n: int = 5) -> list[tuple[str, float]]:
        """Return top-n features by importance."""
        importances = list(zip(self.feature_names, self.feature_importances))
        return sorted(importances, key=lambda x: x[1], reverse=True)[:n]


def train_surrogate_model(
    X:            np.ndarray,
    y:            np.ndarray,
    xgb_params:   Optional[dict] = None,
    cv_folds:     int = SURROGATE_CV_FOLDS,
) -> SurrogateModel:
    """
    Train and cross-validate the XGBoost surrogate model.

    Pipeline:
        StandardScaler → XGBRegressor

    The StandardScaler is included even though XGBoost is theoretically
    scale-invariant for tree-based splits, because:
        1. The pipeline may be compared with scale-sensitive baselines.
        2. It ensures the feature ranges are stable if the model is
           retrained on different circuit data.
        3. Negligible computational cost.

    Cross-validation reports both MAE (in seconds — directly interpretable)
    and R² (proportion of variance explained — architecture quality signal).

    Args:
        X:          Feature matrix from generate_training_data().
        y:          Target race times from generate_training_data().
        xgb_params: XGBoost hyperparameters. Defaults to XGBOOST_DEFAULTS.
        cv_folds:   KFold cross-validation splits.

    Returns:
        Fitted SurrogateModel.

    Raises:
        ImportError: If XGBoost is not installed.
        ValueError:  If X and y have incompatible shapes.
    """
    if not XGBOOST_AVAILABLE:
        raise ImportError(
            "XGBoost is not installed. "
            "Run: pip install xgboost>=2.0.0"
        )

    if X.shape[0] != len(y):
        raise ValueError(
            f"train_surrogate_model: X has {X.shape[0]} rows but "
            f"y has {len(y)} elements."
        )

    params = {**XGBOOST_DEFAULTS, **(xgb_params or {})}

    regressor = xgb.XGBRegressor(**params)
    pipeline  = Pipeline([
        ("scaler",     StandardScaler()),
        ("regressor",  regressor),
    ])

    X_df = pd.DataFrame(X, columns=STRATEGY_FEATURE_NAMES)

    logger.info(
        "train_surrogate_model: training XGBoost on %d samples | "
        "n_estimators=%d  max_depth=%d  lr=%.3f",
        len(X), params["n_estimators"],
        params["max_depth"], params["learning_rate"],
    )

    # Cross-validation (KFold — regression, so no stratification needed)
    kf = KFold(n_splits=cv_folds, shuffle=True, random_state=RANDOM_STATE)

    cv_mae = cross_val_score(
        pipeline, X_df, y, cv=kf,
        scoring="neg_mean_absolute_error", n_jobs=-1,
    )
    cv_mae = -cv_mae  # Convert from negative MAE to positive

    cv_r2  = cross_val_score(
        pipeline, X_df, y, cv=kf,
        scoring="r2", n_jobs=-1,
    )

    logger.info(
        "train_surrogate_model: CV MAE = %.2f ± %.2f s | "
        "CV R² = %.4f ± %.4f | fold MAEs: %s",
        cv_mae.mean(), cv_mae.std(),
        cv_r2.mean(),  cv_r2.std(),
        [f"{m:.2f}" for m in cv_mae],
    )

    if cv_mae.mean() > MAX_ACCEPTABLE_MAE_SEC:
        logger.warning(
            "train_surrogate_model: CV MAE %.2fs > %.1fs threshold. "
            "The surrogate may not reliably distinguish strategies "
            "separated by < %.1fs. Consider increasing training samples "
            "or retuning XGBoost hyperparameters.",
            cv_mae.mean(), MAX_ACCEPTABLE_MAE_SEC, MAX_ACCEPTABLE_MAE_SEC,
        )

    # Final fit on full training set
    pipeline.fit(X_df, y)

    # Extract feature importances from the underlying XGBRegressor
    xgb_step = pipeline.named_steps["regressor"]
    importances = getattr(xgb_step, "feature_importances_", np.zeros(X.shape[1]))

    model = SurrogateModel(
        pipeline            = pipeline,
        feature_names       = STRATEGY_FEATURE_NAMES,
        cv_scores_mae       = cv_mae,
        mean_cv_mae         = float(cv_mae.mean()),
        std_cv_mae          = float(cv_mae.std()),
        cv_scores_r2        = cv_r2,
        mean_cv_r2          = float(cv_r2.mean()),
        training_samples    = len(X),
        feature_importances = importances,
        y_train_mean        = float(y.mean()),
        y_train_std         = float(y.std()),
    )

    logger.info("train_surrogate_model: %s", model.summary())
    logger.info(
        "train_surrogate_model: top features: %s",
        [(f, f"{imp:.4f}") for f, imp in model.top_features(5)],
    )
    return model


