def generate_training_data(
    model_set:          DegradationModelSet,
    base_lap_time_sec:  float,
    total_race_laps:    int,
    available_compounds: list[str],
    sc_probability:     float = 0.68,
    mean_deg_rate:      float = 0.04,
    is_high_deg:        int   = 0,
    is_street:          int   = 0,
    max_samples:        int   = TRAINING_SAMPLES_PER_CIRCUIT,
    pit_lane_delta_sec: float = 19.0,
    rng_seed:           int   = RANDOM_STATE,
) -> tuple[np.ndarray, np.ndarray]:
    """
    Generate (X, y) training data by running the physics simulator
    across a diverse set of strategy configurations.

    Strategy sampling:
        We enumerate ALL valid strategies from enumerate_strategies()
        and then sub-sample max_samples uniformly at random. This
        ensures coverage of all stop counts and compound combinations,
        avoiding a training set dominated by one strategy type.

    Args:
        model_set:           DegradationModelSet for simulation.
        base_lap_time_sec:   Reference lap time.
        total_race_laps:     Race distance.
        available_compounds: Dry compounds available.
        sc_probability:      SC frequency.
        mean_deg_rate:       Mean degradation rate.
        is_high_deg:         High degradation circuit flag.
        is_street:           Street circuit flag.
        max_samples:         Maximum training samples to generate.
        pit_lane_delta_sec:  Pit lane traverse time.
        rng_seed:            Random seed for sub-sampling.

    Returns:
        Tuple (X, y):
            X: float array of shape (n_samples, N_STRATEGY_FEATURES)
            y: float array of shape (n_samples,) — total race times
    """
    from src.strategy_engine.pit_window_optimizer import enumerate_strategies

    logger.info(
        "generate_training_data: enumerating strategies for "
        "laps=%d compounds=%s...",
        total_race_laps, available_compounds,
    )

    all_strategies = enumerate_strategies(
        available_compounds  = available_compounds,
        total_race_laps      = total_race_laps,
        pit_lane_delta_sec   = pit_lane_delta_sec,
    )

    logger.info(
        "generate_training_data: %d strategies enumerated — simulating...",
        len(all_strategies),
    )

    # Sub-sample if needed
    rng = np.random.default_rng(rng_seed)
    if len(all_strategies) > max_samples:
        indices       = rng.choice(len(all_strategies), size=max_samples, replace=False)
        all_strategies = [all_strategies[i] for i in sorted(indices)]
        logger.info(
            "generate_training_data: sub-sampled to %d strategies.", max_samples
        )

    X_rows: list[np.ndarray] = []
    y_vals: list[float]      = []
    n_invalid = 0

    for strategy in all_strategies:
        result: SimulationResult = simulate_strategy(
            strategy          = strategy,
            model_set         = model_set,
            base_lap_time_sec = base_lap_time_sec,
            total_race_laps   = total_race_laps,
        )
        if not result.is_valid:
            n_invalid += 1
            continue

        feat = encode_strategy(
            strategy         = strategy,
            total_race_laps  = total_race_laps,
            base_lap_time    = base_lap_time_sec,
            sc_probability   = sc_probability,
            mean_deg_rate    = mean_deg_rate,
            is_high_deg      = is_high_deg,
            is_street        = is_street,
        )
        X_rows.append(feat)
        y_vals.append(result.total_race_time_sec)

    if not X_rows:
        raise RuntimeError(
            "generate_training_data: no valid simulation results. "
            "Check DegradationModelSet and strategy configuration."
        )

    X = np.vstack(X_rows)
    y = np.array(y_vals, dtype=np.float64)

    logger.info(
        "generate_training_data: %d training samples generated "
        "(%d invalid skipped) | y range=[%.1f, %.1f]s",
        len(X), n_invalid, y.min(), y.max(),
    )
    return X, y


